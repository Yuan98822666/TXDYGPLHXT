"""
文件路径：app/utils/stock_type.py
作用说明：A股板块分类工具

分类依据：
  - 通过 stock_code（股票代码）+ exchange（交易所标识）判断板块类型
  - 不需要额外接口，纯本地计算

板块分类规则：
  ┌──────────┬───────────────┬─────────┬──────────────────┐
  │ 板块名称  │  代码范围       │ exchange │ 标识代码          │
  ├──────────┼───────────────┼─────────┼──────────────────┤
  │ 沪市主板  │ 600000~603999 │    1     │ SH_ZB            │
  │ 科创板    │ 688000~688999 │    1     │ KCB              │
  │ 深市主板  │ 000000~002999 │    0     │ SZ_ZB            │
  │ 创业板    │ 300000~301999 │    0     │ CYB              │
  │ 北交所    │ 830000~839999 │    0     │ BJS              │
  └──────────┴───────────────┴─────────┴──────────────────┘

  优先级：科创板 > 沪市主板 > 创业板 > 深市主板 > 北交所

  注意：深市主板代码 000xxx 和 001xxx 为早期老股票，目前基本无交易
"""

from typing import Optional


def get_stock_type(stock_code: str, exchange: Optional[str] = None) -> str:
    """
    根据股票代码判断A股所属板块

    参数:
        stock_code: 股票代码（6位数字字符串，如 "600519"）
        exchange:   交易所标识（"0"=深市，"1"=沪市），可选

    返回:
        str: 板块标识代码
            SH_ZB  → 沪市主板
            KCB    → 科创板
            SZ_ZB  → 深市主板
            CYB    → 创业板（ChiNext）
            BJS    → 北交所（北京证券交易所）
            ""     → 未能识别的代码（可能是B股、债券、基金等）

    示例:
        get_stock_type("600519", "1")  → "SH_ZB"（沪市主板，贵州茅台）
        get_stock_type("688041", "1")  → "KCB"（科创板，海光信息）
        get_stock_type("000001", "0")  → "SZ_ZB"（深市主板，平安银行）
        get_stock_type("300750", "0")  → "CYB"（创业板，宁德时代）
        get_stock_type("830946", "0")  → "BJS"（北交所）
    """
    if not stock_code:
        return ""

    # 统一转为字符串，去除空格
    code = str(stock_code).strip()

    # 长度校验（必须是6位数字）
    if len(code) != 6 or not code.isdigit():
        return ""

    # 转换为整数便于范围比较
    try:
        code_num = int(code)
    except ValueError:
        return ""

    # ─── 沪市股票（exchange=1）───────────────────────────────────
    if exchange == "1":
        # 科创板：688000~688999
        if 688000 <= code_num <= 688999:
            return "科创板"
        # 沪市主板：600000~603999（包含早期的 605xxx、604xxx）
        if 600000 <= code_num <= 609999:
            return "深圳主板"
        # 沪市B股：900000~905xxx（不采集，跳过）
        if 900000 <= code_num <= 905999:
            return ""

    # ─── 深市股票（exchange=0）───────────────────────────────────
    if exchange == "0":
        # 创业板：300000~301999
        if 300000 <= code_num <= 301999:
            return "创业板"
        # 深市主板：000000~002999（000xxx为老股，001xxx极少）
        if 0 <= code_num <= 299999:
            return "上证主板"
        # 北交所：830000~839999（83/832/833/835/836/837/838/839 开头）
        if 830000 <= code_num <= 839999:
            return "北交所"
        # 深市B股：200000~200xxx（不采集，跳过）
        if 200000 <= code_num <= 209999:
            return ""

    # ─── 其他情况：未能识别的代码 ───────────────────────────────
    # 可能是老三板（400/430xxx）、债券、基金、期权等
    return ""
